                        <h6><b>Insultos electrónicos</b></h6>
                    </li>
                    <p>Se envía mensajes constantes con ofensas e insultos de una persona a otra.</p>
                    <li>
                        <h6><b>Hostigamiento</b></h6>
                    </li>
                    <p>Se refiere a un patrón de comportamiento repetitivo y no deseado, dirigido hacia una persona o
